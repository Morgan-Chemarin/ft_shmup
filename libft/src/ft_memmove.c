#include "libft.h"

void	*ft_memmove(void *dest, const void *src, size_t n)
{
	unsigned char	*cdest;
	unsigned char	*csrc;
	size_t			i;

	cdest = (unsigned char *)dest;
	csrc = (unsigned char *)src;
	i = 0;
	if (cdest < csrc)
	{
		while (i < n && csrc[i - 1])
		{
			((char *)dest)[i] = ((char *)src)[i];
			i++;
		}
		ft_bzero(dest + i, n - i);
	}
	else
	{
		cdest += n - 1;
		csrc += n - 1;
		while (n--)
			*cdest-- = *csrc--;
	}
	return (dest);
}
